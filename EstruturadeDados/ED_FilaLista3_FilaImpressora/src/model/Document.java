package model;

public class Document {
	String id;
	String name;
	int time;


public Document() {
	this.name= name;
	this.id = id;
	this.time = time;
}

	public String getName() {
	    return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id =id;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
}

